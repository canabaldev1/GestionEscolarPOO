    private void cargarTablaAlumnos(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_cargarTablaAlumnos
        // TODO add your handling code here:
        try {
            List<CicloAcademico> lista = CicloAcademicoDto.listarTodo();  // Obtener lista de cicloAcademicos

            DefaultTableModel modelo = (DefaultTableModel) tablaCiclosAcademicos.getModel();
            modelo.setRowCount(0); // Limpiar la tabla antes de llenar

            if (lista.isEmpty()) {
                JOptionPane.showMessageDialog(this, "No hay elementos que mostrar");
            }

            for (CicloAcademico a : lista) {
                System.out.println("Agregando: " + a.getAnio());
                Object[] fila = {
                    a.getId(),
                    a.getAnio(),
                    a.getFechaInicio(),
                    a.getFechaFin(),
                    a.getEstado()
                };
                modelo.addRow(fila);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error al cargar Ciclos Academicos: " + e.getMessage());
            e.printStackTrace();
        }
    }//GEN-LAST:event_cargarTablaAlumnos

