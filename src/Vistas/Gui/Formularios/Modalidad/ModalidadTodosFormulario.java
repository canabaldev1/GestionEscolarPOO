    private void cargarTabla(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_cargarTabla
        // TODO add your handling code here:

        try {
            List<Modalidad> lista = ModalidadDto.listarTodo();  // Obtener lista de alumnos

            DefaultTableModel modelo = (DefaultTableModel) tablaModalidad.getModel();
            modelo.setRowCount(0); // Limpiar la tabla antes de llenar

            if (lista.isEmpty()) {
                JOptionPane.showMessageDialog(this, "No hay elementos que mostrar");
            }

            for (Modalidad a : lista) {

                String aNivelEducativo = "";
                if (a.getNivelEducativo() != null) {
                    aNivelEducativo = (a.getNivelEducativo().getId() + " - " + a.getNivelEducativo().getNombre());

                }

                Object[] fila = {
                    a.getId(),
                    a.getNombre(),
                    a.getDescripcion(),
                    aNivelEducativo};
                modelo.addRow(fila);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error al cargar niveles educativos: " + e.getMessage());
            e.printStackTrace();
        }
    }//GEN-LAST:event_cargarTabla

