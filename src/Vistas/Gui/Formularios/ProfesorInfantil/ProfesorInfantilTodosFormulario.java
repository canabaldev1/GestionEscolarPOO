    private void cargarDatosTabla(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_cargarDatosTabla
        // TODO add your handling code here:

        try {
            List<ProfesorInfantil> lista = ProfesorInfantilDto.listarTodo();  // Obtener lista de profesorInfantils

            DefaultTableModel modelo = (DefaultTableModel) tabla.getModel();
            modelo.setRowCount(0); 

            if (lista.isEmpty()) {
                JOptionPane.showMessageDialog(this, "No hay elementos que mostrar");
            }

            for (ProfesorInfantil a : lista) {
                System.out.println("Agregando: " + a.getNombres() + " " + a.getApellidos());
                Object[] fila = {
                    a.getId(),
                    a.getNombres(),
                    a.getApellidos(),
                    a.getContrasena(),
                    a.getEmail(),
                    a.getTelefono(),
                    a.getDireccion(),
                    a.getFechaNacimiento(),
                    a.getProfesion(),
                    a.getNumeroCuentaBancaria(),
                    a.getTipoCuentaBancaria(),
                    a.getBancoAsociado(),
                    a.getFechaIngreso(),
                    a.getEstado()
                // Puedes agregar más campos si tienes más columnas
                };
                modelo.addRow(fila);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error al cargar profesores: " + e.getMessage());
            e.printStackTrace();
        }
    }//GEN-LAST:event_cargarDatosTabla

